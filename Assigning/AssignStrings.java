package Assigning;

public class AssignStrings {
    public static void main(String[] args){
        String text = "Hello";
        String blank = " ";
        String name = "Madhav";
        int myInt = 9;
        System.out.println(text);
        System.out.println(text + blank + name);
        System.out.println("Hello" + " " + "Bob");
        System.out.println("My integer is: " + myInt);
    }
}
